public class City
{
    public double MinLat, MaxLat, MinLng, MaxLng;
    public string CityName;

    public City(string cityName, double minLat, double maxLat, double minLng, double maxLng)
    {
        CityName = cityName;
        MinLat = minLat;
        MaxLat = maxLat;
        MinLng = minLng;
        MaxLng = maxLng;


    }





}